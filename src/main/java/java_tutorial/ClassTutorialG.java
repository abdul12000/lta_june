package java_tutorial;

public class ClassTutorialG {


    public static void main(String[] args) {
        //while loop
        int i = 20;
        while (i < 40){
            System.out.println("Value of i is : " + i);
            i++;
        }

        //do while
        int x = 20;
        do{
            System.out.println(("The value of x is : " + x));
            x++;
        }
        while (x < 40);
    }
}
