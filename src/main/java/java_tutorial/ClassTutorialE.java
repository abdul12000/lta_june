package java_tutorial;

public class ClassTutorialE {
    //for loop

    public static void main(String[] args) {

        //going in ascending order
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        for (int i = 0; i != 8; i++) {
            System.out.println(i);
        }

        //20, 19, 18, 17, 16, 15, 14, 13, 12, 11
        //Descending order
        for (int i = 20; i > 0; i--) {
            System.out.println(i);
        }
    }
}
