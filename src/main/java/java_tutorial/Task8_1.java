package java_tutorial;

public class Task8_1 {
//    1.	Write a java program to take 2 integers (int A and int B) where A = 100 and B = 50.
//    Then evaluate the values of A and B, and printout value of A if A is greater than B. Otherwise printout value of B.

    //Short cut to format your opened file - Ctrl+Windows+Alt+L

    public static void main(String[] args) {
        int A = 100;
        int B = 50;
        if (A > B) {
            System.out.println(A);
        } else {
            System.out.println(B);
        }
    }
}
